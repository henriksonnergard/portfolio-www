          <h4>Project description</h4>
          <p>As the need for a good way of presenting different and completely unrelated projects emerged, I came to the conclusion that my old resume page <a href="http://henrik.sonnergard.com/">henrik.sonnergard.com</a> needed to be revamped. It was only one single static page, didn't look good on mobile phones and basically consisted of text rather than actually showing what I do.</p>
          <p>The first sketches of the new design was a simple first page where all of the projects were displayed as small Polaroid photos on a table, with the idea that a closer look on each photo would reveal more details about the project. I tried a few different approaches. One where additional info would pop out on a paper below the selected project while pushing away the other photos. One where additional info showed up in a speech bubble from the selected project. As fun and aesthetically pleasing these ideas might have been, the choice fell on the simplest approach: a stand-alone project page. It's more focused on the selected project, more convenient to read and makes it possible to share or bookmark the link. And definitely much easier to build.</p>
          <p>Another bullet point was to to make it adapt to different screen resolutions, so the website is usable on desktop and laptop computers as well as tablets and smartphones. The choice fell on <a href="http://getskeleton.com/">Skeleton</a>, a light-weight framework which separates the content in columns. It's a solid foundation that works really well and is easy to get up-and-running.</p>
